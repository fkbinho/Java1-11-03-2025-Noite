package tipo_de_dados;

public class Dados {
	
	// Método principal
	public static void main(String[] args) {
		
		// Tipos de dados
		
		// númericos
		int idade = 28;
		double altura = 1.78;

		System.out.println(idade);
		System.out.println(altura);
		
		// textuais
		char letra = 'A';
		String nome = "Wilson";
		
		System.out.println(nome);
		System.out.println(letra);
		
		// booleano
		
		// true ou false
		boolean estaFrio = true;
		
		System.out.println(estaFrio);
		
	}

}
