package revisao;

public class RevisaoNoite {
	// método principal
	public static void main(String[] args) {
		
		String mensagem; // declaração de variável
		mensagem = "Olá pessoal,"; // atribuindo valor
		
		String mensagem2 = "Sejam bem vindos."; // inicializando a variável
		
		System.out.println(mensagem);
		System.out.println(mensagem2);
		
		// tipo de dados primitivos
		
		//tipo de dados numéricos
		int num = 655;
		long num2 = 5485;
		float num3 = 4.85f;
		double num4 = 47.5;	
		
		//tipo alfanumérico
		char sexo = 'm';
		String nome = "Tadeu";
		
		// booleanos ( true e false )
		boolean eNoite = true;
		
		System.out.println(num);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		System.out.println(sexo);
		System.out.println(nome);
		System.out.println(eNoite);

	}

}
