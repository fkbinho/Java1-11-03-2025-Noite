package operadores;

public class Relacionais {

	public static void main(String[] args) {

		int a = 8;
		int b = 7;
		
		boolean eMaior;
		
		eMaior = a > b;
		System.out.println(eMaior);

		eMaior = a >= b;
		System.out.println(eMaior);
		
		eMaior = a < b;
		System.out.println(eMaior);

		eMaior = a <= b;
		System.out.println(eMaior);

		eMaior = a == b;
		System.out.println(eMaior);

		eMaior = a != b;
		System.out.println(eMaior);

	}

}
